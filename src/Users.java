    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        String first_name = jTextField3.getText();
        String last_name = jTextField2.getText();
        
        try{
            /**
             * get the how many row (auto increment trick)
             */
            int count=0;
            String query = "SELECT Count(*) as rowcount FROM USERS";
            stmt = con.prepareStatement(query);
            rs = stmt.executeQuery();
            if(rs.next()){
                count = rs.getInt("rowcount")+1;
            }
            //System.out.print(count);
            /**
             * insert new product
             */
            String q = "INSERT INTO USERS VALUES(?,?,?,?,?)";
            stmt = con.prepareStatement(q);
            stmt.setInt(1, count);
            stmt.setString(2,username);
            stmt.setString(3,password);
            stmt.setString(4,last_name);
            stmt.setString(5,first_name);
            stmt.executeUpdate();   
            populateTable();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

